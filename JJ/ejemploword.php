EL BALONCESTO EN COLOMBIA
No existe un documento oficial que nos permita tener una fecha y
un relato exacto acerca de la llegada del baloncesto a nuestro país; 
Se tejen varias versiones una de ellas dice que el baloncesto fue traído por jóvenes estudiantes colombianos
que se encontraban en Estados Unidos y regresaron en 1908 a continuar sus estudios en Barranquilla (Colegio Americano) 
También pudo llegar por Cartagena o Buenaventura, traído por los infantes de marina, 
quienes lo dieron a conocer primero en esas regiones costaneras, para luego pasar al interior. 
Año 1910. Y la última que parece ser la más acertada nos dice que en el año 1925, el hermano 
francés Janebaud y don David Martínez Collazos, siendo éste último profesor de la salle, en Santander,
dieron a conocer las primeras nociones del Baloncesto en Colombia. El juego se difundió en los centros
educativos principalmente dirigidos por los hermanos cristianos. Don David Martínez fue un fervoroso apóstol
del Baloncesto en el Oriente Colombiano con sede en Bucaramanga; a él le correspondió el mérito de haber
traído al país los elementos esenciales para la práctica de este deporte, tales como las canastas, balones y 
el reglamento oficial. Dedico la mayor parte de su fortuna a la divulgación de esta actividad. 
El 18 de Julio de 1925, con motivo de las fiestas patrias se llevo a cabo el primer 
partido de Baloncesto en Colombia entre dos equipos del Liceo de la Salle en predios del mismo plantel.
Posteriormente se practica en el Instituto Técnico Central de Bogotá, en el colegio San Pedro Claver de Bucaramanga,
y el colegio San José de Guanenta en San Gil. Desde entonces el Baloncesto ha sido incluido en el programa de todos los eventos Nacionales 
e Internacionales celebrado en Colombia. Es así como en el marco de lo que en Colombia en sus inicios se llamaron los Juegos Olímpicos 
(hoy Juegos Atléticos Nacionales), el campeonato de baloncesto es desarrollado en el colegio la Salle en 1927, 
los participantes fueron divididos en dos grupos en el A estaban la Escuela Normal Central, San Luis Gonzaga de Zipaquira, 
el Instituto Técnico Central, el Instituto la salle I, la Escuela Militar, la Salle II y el Instituto Técnico II; 
En el grupo B la escuela Normal Magdalena, el Instituto Técnico I, Instituto la Salle I, Instituto la Salle II y la escuela de Medicina. 
El Campeón Olímpico del grupo A fue el Instituto Técnico Central que en la final venció al equipo de la escuela Central, 
el equipo campeón estaba conformado por Carlos Parra, Manuel J de Bedout, Guillermo González, Jaime Ramírez y Gustavo Wolf. 
En el grupo B el campeón fue el equipo de la Salle venciendo a la Escuela de Medicina. En 1928, en el mes de Octubre 
El Club Tequendama organizó un partido en la categoría infantil entre los equipos Tequendama Junior y Santander B.B.C categoría infantil, 
el ganador fue el quinteto Santander BBC, formaban cabe destacar en ese entonces a los jugadores infantiles Gabriel Herrera, 
A. Henao, A. Izaza, J Henao, G. Clevez, y M. Aranguren. Quienes pertenecían a la nómina del tequendama Junior. En Bogotá, 
algunos clubes sociales y colegios religiosos REALIZAN EL PRIMER CAMPEONATO NACIONAL INTERCOLEGIADO EN 1930. El 7 de Abril de 1930, 
se disputa la Copa Sofía Urrutia entre los quintetos Diablos Rojos y Country Club. El primer partido entre un equipo nacional y uno 
extranjero se realiza en la cancha del Club deportivo Tequendama, el 4 de Julio de 1931. Con la participación de los equipos: América I, 
América II, Country Club y Friends, la asociación Femenina de deportes, organizó el Campeonato Bogotano femenino de Baloncesto, 
campeón fue el equipo del Country Club frente al América I. El redactor deportivo Luis Guillermo Forero, da a conocer 
el reglamento versión española, en 1934, este fue el segundo reglamento después de la traducción realizada por Billy Sport en Cartagena en año 1929. 
En 1936 surge la necesidad de una Asociación que agremiaría a todos los participantes, así nace la Asociación Colombiana de Baloncesto, 
que más tarde sería la Federación Colombiana de Baloncesto. El Instituto Nacional de Educación Física, que por ese entonces funcionaba en 
la Universidad Nacional de Colombia, con sede en Bogotá, inicia la formación de profesores de Educación física, el baloncesto firmó parte del plan de estudios. 
(1936) Bogotá en 1938 es sede de los juegos Bolivarianos en conmemoración del IV Centenario de la fundación de Bogotá; 
El profesor Mexicano López Hinojosa, fue pilar importante en el desarrollo del Baloncesto en los juegos; Colombia sale campeón en femenino y masculino. 
El equipo femenino estaba dirigido por Erasmo López y conformado por las jugadoras Nicolasa Manotas, Lucy Tobon de la roche, Conchita Gutierez, Elisa salcedo, 
Emma Murcia, Beatriz Angarita, Judith Casasbuenas, Matilde Robledo. 